void toCvMat(float *data, cv::Mat &image) {

  for (int j = 0; j < image.rows; ++j) {
    for (int i = 0; i < image.cols; ++i) {
      (image.at<uchar>(j, i)) = (uchar) data[j * image.cols + i];
    }
  }

//  memcpy((float * )image.data, data, image.rows * image.cols*sizeof(float) );

};



